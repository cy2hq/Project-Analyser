 /* MISSING RECORDS VIEWS IN PROJECT  */
     SELECT A.PROJECTNAME, B.RECNAME
  FROM PSPROJECTITEM A, PSRECDEFN B
  WHERE  A.OBJECTTYPE = 0
     AND A.OBJECTVALUE1 = B.RECNAME
     AND B.RECTYPE = 1
     AND A.PROJECTNAME LIKE 'CY2_CONFIG%' 
     and not exists(SELECT 'X'
  FROM PSPROJECTITEM C
  WHERE C.OBJECTTYPE = 30
     AND C.OBJECTID1 = 65
     AND C.OBJECTVALUE2 = 2
     AND C.OBJECTVALUE1 = B.RECNAME
    AND C.PROJECTNAME =  A.PROJECTNAME);