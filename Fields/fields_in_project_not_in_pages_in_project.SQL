/* FIELDS IN THE PROJECT THAT ARE NOT IN PAGES IN THE PROJECT */
SELECT A.PROJECTNAME, B.FIELDNAME
  FROM PSPROJECTITEM A, PSDBFIELD B
  WHERE  A.OBJECTTYPE = 2
     AND A.OBJECTVALUE1 = B.FIELDNAME
     AND A.OBJECTID2 = 0
     AND A.PROJECTNAME = 'CY2_AD_APPL_001' 
     AND NOT EXISTS(SELECT 'X' FROM 
  PSPNLFIELD C, PSPROJECTITEM D
  WHERE  D.OBJECTTYPE = 5
     AND C.PNLNAME = D.OBJECTVALUE1
     AND B.FIELDNAME = C.FIELDNAME 
     AND A.PROJECTNAME = D.PROJECTNAME)